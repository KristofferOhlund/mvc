     * Query Rooms by name
     * @return Room|null
     */
    public function findRoomByName(string $value): ?Room
    {
        return $this->createQueryBuilder('b')
            ->setParameter('val', $value)
            ->andWhere('b.name = :val')
            ->getQuery()
            ->getOneOrNullResult()
